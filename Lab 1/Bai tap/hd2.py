# Thực hiện 4 phép tính số học ( + - * / )
a = float(input("Nhập a: "))
b = float(input("Nhập b: "))
cong = a + b
tru = a - b
nhan = a * b
chia = a / b
print("Phép cộng của ", a , "và ", b ,"là: ", cong)
print("Phép trừ của ", a , "và ", b ,"là: ", tru)
print("Phép nhân của ", a , "và ", b ,"là: ", nhan)
print("Phép chia của ", a , "và ", b ,"là: ", chia)